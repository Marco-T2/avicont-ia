export interface JournalProps {
  id: string;
  organizationId: string;
  status: JournalEntryStatus;
  // `number` is the auto-generated sequential voucher number. The Prisma adapter
  // (C3) assigns it during insert via retry loop (VOUCHER_NUMBER_CONTENTION);
  // pre-persistence the field is null. This is analogous to Payment.journalEntryId.
  number: number | null;
  referenceNumber: number | null;
  date: Date;
  description: string;
  periodId: string;
  voucherTypeId: string;
  contactId: string | null;
  // `sourceType` distinguishes manual (null) from auto-generated entries
  // (sale/purchase/payment/ai). I9 forbids mutators when sourceType !== null.
  sourceType: string | null;
  sourceId: string | null;
  aiOriginalText: string | null;
  createdById: string;
  updatedById: string | null;
  createdAt: Date;
  updatedAt: Date;
  lines: JournalLine[];
}

export interface JournalLineDraft {
  accountId: string;
  side: LineSide;
  description?: string | null;
  contactId?: string | null;
}

export interface UpdateJournalInput {
  description?: string;
  date?: Date;
  referenceNumber?: number | null;
  contactId?: string | null;
  updatedById?: string;
  // `sourceType`/`sourceId`/`aiOriginalText` son inmutables post-creación
  // (I9). No se exponen en el input de update.
  // Las líneas se reemplazan por `replaceLines()`, no por `update()`.
}

export interface CreateJournalInput {
  organizationId: string;
  date: Date;
  description: string;
  periodId: string;
  voucherTypeId: string;
  createdById: string;
  contactId?: string | null;
  referenceNumber?: number | null;
  sourceType?: string | null;
  sourceId?: string | null;
  aiOriginalText?: string | null;
  lines?: JournalLineDraft[];
}

